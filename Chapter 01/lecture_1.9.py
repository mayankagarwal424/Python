# Arithmetic Opreator (+, -, *, /, //, %, **)
print("Adition", 2 + 3)
print("Substraction", 9 - 1)
print("Multiplication", 5 * 3)
print("Division", 7 / 3) 
print("Floor Division", 7 // 3)    #it give integer part of division.
print("Modulus", 7 % 3)            #it give remender part of division.
print("Exponentiation",8 ** 3)     #it use as power.

#Relational Opreater (>, <, <=, >=, ==, !=)
print(5 > 3)
print(4 < 8)
print(10 >= 8)
print(7 <= 6)
print(8 == 8)
print(4 != 5)

#logical opreater (and,or,not)
print(True and False)
print(True and True)
print(False and True)
print(False and False)

print(True or False)
print(True or True)
print(False or True)
print(False or False)

print(not (False))
print(not True)